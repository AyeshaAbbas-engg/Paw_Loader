def download_one(url, index, total):
    is_playlist = playlist_mode == "Full playlist"

    folder = os.path.join(
        DOWNLOAD_ROOT,
        datetime.now().strftime("%Y-%m-%d")
    )
    os.makedirs(folder, exist_ok=True)

    def hook(d):
        if d["status"] == "downloading":
            try:
                percent = float(
                    d.get("_percent_str", "0%").replace("%", "").strip()
                )
                progress_bar.progress(int(percent))
                status_text.info(
                    f"â¬‡ {percent:.1f}% | "
                    f"{d.get('_speed_str','')} | "
                    f"ETA {d.get('_eta_str','')}"
                )
            except:
                pass

        elif d["status"] == "finished":
            status_text.warning("âš™ï¸ Finalizing download...")

    # ğŸ” NO FFMPEG REQUIRED FORMAT
    ydl_opts = {
        "outtmpl": f"{folder}/%(title)s.%(ext)s",
        "format": "best[ext=mp4]/best",
        "noplaylist": not is_playlist,
        "progress_hooks": [hook],
        "quiet": True,
        "no_warnings": True,
        "retries": 5,
    }

    try:
        with yt_dlp.YoutubeDL(ydl_opts) as ydl:
            ydl.download([url])
            log(f"âœ… Downloaded: {url}")
            return True
    except Exception as e:
        log(f"âŒ Failed: {str(e)}")
        return False

def start_download():
    st.session_state.downloading = True
    total = len(st.session_state.queue)

    for i, url in enumerate(st.session_state.queue, 1):
        status_text.info(f"ğŸ¾ Video {i} of {total}")
        download_one(url, i, total)
        progress_bar.progress(0)

    st.session_state.queue.clear()
    st.session_state.downloading = False
    status_text.success("ğŸ‰ All downloads completed!")

